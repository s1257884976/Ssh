package com.jiang.model;

public class user {
	private Integer id;
	private String name;
	private Integer pad;
	private String phone;
	private String juese;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the pad
	 */
	public Integer getPad() {
		return pad;
	}
	/**
	 * @param pad the pad to set
	 */
	public void setPad(Integer pad) {
		this.pad = pad;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the juese
	 */
	public String getJuese() {
		return juese;
	}
	/**
	 * @param juese the juese to set
	 */
	public void setJuese(String juese) {
		this.juese = juese;
	}
	
	
	
}
